const parseVoiceWorkout = (text: string): WorkoutExercise[] => {
  const exercises: WorkoutExercise[] = [];
  const lower = text.toLowerCase();
  const muscleMap: Record<string, string> = {
    'bench': 'Chest', 'chest': 'Chest', 'push up': 'Chest', 'pushup': 'Chest',
    'pull up': 'Back', 'pullup': 'Back', 'deadlift': 'Back', 'row': 'Back', 'lat': 'Back',
    'squat': 'Legs', 'leg': 'Legs', 'lunge': 'Legs', 'calf': 'Legs',
    'shoulder': 'Shoulder', 'overhead': 'Shoulder', 'ohp': 'Shoulder', 'lateral': 'Shoulder',
    'curl': 'Biceps', 'bicep': 'Biceps',
    'tricep': 'Triceps', 'dip': 'Triceps', 'extension': 'Triceps',
    'plank': 'Core', 'crunch': 'Core', 'ab': 'Core', 'core': 'Core',
    'run': 'Cardio', 'walk': 'Cardio', 'jump': 'Cardio', 'burpee': 'Cardio',
  };
  const patterns = [
    /(\d+)\s*sets?\s+(\d+)\s*reps?\s+(.+?)\s+(\d+)\s*kg/gi,
    /(\d+)\s*sets?\s+(.+?)\s+(\d+)\s*reps?\s+(\d+)\s*kg/gi,
    /(.+?)\s+(\d+)\s*sets?\s+(\d+)\s*reps?\s+(\d+)\s*kg/gi,
    /(\d+)\s*x\s*(\d+)\s+(.+?)\s+(\d+)\s*kg/gi,
    /(.+?)\s+(\d+)\s*x\s*(\d+)\s+(\d+)\s*kg/gi,
  ];
  let matched = false;
  for (const pattern of patterns) {
    let m;
    pattern.lastIndex = 0;
    while ((m = pattern.exec(lower)) !== null) {
      const groups = m.slice(1).filter(Boolean);
      if (groups.length >= 3) {
        const nums = groups.filter(g => /^\d+$/.test(g)).map(Number);
        const name = groups.find(g => /[a-z]/.test(g)) || 'Exercise';
        if (nums.length >= 2) {
          const muscle = Object.entries(muscleMap).find(([k]) => name.includes(k))?.[1] || 'Full Body';
          exercises.push({ name: name.replace(/\b\w/g, c => c.toUpperCase()).trim(), sets: nums[0] || 3, reps: nums[1] || 10, weight: nums[2] || 0, unit: 'kg', muscle });
          matched = true;
        }
      }
    }
    if (matched) break;
  }
  if (!matched) {
    const simplePattern = /(\d+)\s+([a-z\s]+?)(?:\s|$|,|aur|and)/gi;
    let m2;
    while ((m2 = simplePattern.exec(lower)) !== null) {
      const reps = parseInt(m2[1]);
      const name = m2[2].trim();
      if (reps > 0 && name.length > 2) {
        const muscle = Object.entries(muscleMap).find(([k]) => name.includes(k))?.[1] || 'Full Body';
        exercises.push({ name: name.replace(/\b\w/g, c => c.toUpperCase()), sets: 1, reps, weight: 0, unit: 'bw', muscle });
      }
    }
  }
  return exercises.length > 0 ? exercises : [{ name: text.replace(/\b\w/g, c => c.toUpperCase()).slice(0, 30), sets: 3, reps: 10, weight: 0, unit: 'bw', muscle: 'Full Body' }];
};

const analyzeWorkout = (exercises: WorkoutExercise[]): string => {
  if (!exercises.length) return '';
  const muscles = [...new Set(exercises.map(e => e.muscle))];
  const totalSets = exercises.reduce((a, e) => a + e.sets, 0);
  const totalReps = exercises.reduce((a, e) => a + e.sets * e.reps, 0);
  const hasWeight = exercises.some(e => e.weight > 0);
  let analysis = `💪 ${exercises.length} exercise${exercises.length > 1 ? 's' : ''} detected!\n`;
  analysis += `📊 Total: ${totalSets} sets, ${totalReps} reps\n`;
  analysis += `🎯 Muscles: ${muscles.join(', ')}\n`;
  if (hasWeight) analysis += `🏋️ Weighted exercises included\n`;
  if (muscles.includes('Chest') && muscles.includes('Back')) analysis += `\n✅ Balanced push-pull workout!`;
  else if (muscles.length === 1) analysis += `\n💡 Try adding complementary muscle groups next time!`;
  if (totalSets > 15) analysis += `\n🔥 High volume workout — great effort!`;
  else if (totalSets < 6) analysis += `\n💡 Consider adding more sets for better results.`;
  return analysis;
};

