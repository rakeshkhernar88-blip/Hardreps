const parseSteps = (data: any) => {
  if (!data?.bucket) return { today: 0, weeklyData: Array(7).fill(0) as number[], goal: 10_000 };
  const raw = data.bucket.map((b: any) => b.dataset?.[0]?.point?.[0]?.value?.[0]?.intVal ?? 0);
  while (raw.length < 7) raw.unshift(0);
  const weeklyData = raw.slice(-7);
  return { today: weeklyData[weeklyData.length - 1] || 0, weeklyData, goal: 10_000 };
};

const parseHeartRate = (data: any) => {
  const empty = { current: 0, avg: 0, max: 0, min: 0 };
  if (!data?.bucket) return empty;
  const allDay: number[] = []; const todayR: number[] = [];
  data.bucket.forEach((b: any, idx: number) => {
    for (const p of (b.dataset?.[0]?.point ?? [])) {
      const v = p.value?.[0]?.fpVal;
      if (v == null || v <= 0 || v > 250) continue;
      allDay.push(Math.round(v));
      if (idx === data.bucket.length - 1) todayR.push(Math.round(v));
    }
  });
  if (allDay.length === 0) return empty;
  const avg = Math.round(allDay.reduce((a, b) => a + b, 0) / allDay.length);
  return { current: todayR.length > 0 ? todayR[todayR.length - 1] : avg, avg, max: Math.max(...allDay), min: Math.min(...allDay) };
};

const parseSleepStages = (data: any): NightSleepData[] => {
  if (!data?.bucket) return generateMockSleepData();
  const nights: NightSleepData[] = [];
  for (const bucket of data.bucket) {
    const points = bucket.dataset?.[0]?.point ?? [];
    if (points.length === 0) continue;
    let deep = 0, light = 0, rem = 0, awake = 0;
    for (const pt of points) {
      const type = pt.value?.[0]?.intVal; if (type == null) continue;
      let minutes = 0;
      try { minutes = Math.round(Number((BigInt(pt.endTimeNanos) - BigInt(pt.startTimeNanos)) / BigInt(1_000_000)) / 60); } catch { continue; }
      if (minutes < 0 || minutes > 600) continue;
      switch (type) {
        case 5: case 4: deep += minutes; break;
        case 1: awake += minutes; break;
        case 2: light += minutes; break;
        case 6: rem += minutes; break;
        default: awake += minutes; break;
      }
    }
    const total = deep + light + rem + awake;
    if (total < 30) continue;
    const qualityScore = Math.min(100, Math.round((deep/total)*40+(rem/total)*30+(light/total)*20+Math.max(0,1-awake/total)*10));
    nights.push({ date: new Date(Number(bucket.startTimeMillis)).toLocaleDateString('en-US', { weekday:'short', day:'numeric', month:'short' }), totalHours: parseFloat((total/60).toFixed(1)), qualityScore, stages: [{ stage:'deep', minutes:deep, color:'#4C6EF5', label:'Deep' }, { stage:'rem', minutes:rem, color:'#7C3AED', label:'REM' }, { stage:'light', minutes:light, color:'#6B8EFF', label:'Light' }, { stage:'awake', minutes:awake, color:'#FF6B6B', label:'Awake' }] });
  }
  return nights.length > 0 ? nights : generateMockSleepData();
};

